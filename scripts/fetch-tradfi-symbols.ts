async function main() {
  console.log("Compiling TradFi reserved symbols...");

  // Combine and deduplicate
  const allSymbols = new Set<string>();

  for (const symbol of SP500_SYMBOLS) {
    allSymbols.add(symbol.toUpperCase().replace(".", "")); // Normalize: BRK.B -> BRKB
  }

  for (const symbol of RUSSELL_ADDITIONAL) {
    allSymbols.add(symbol.toUpperCase().replace(".", ""));
  }

  console.log(`S&P 500: ${SP500_SYMBOLS.length} symbols`);
  console.log(`Russell additional: ${RUSSELL_ADDITIONAL.length} symbols`);
  console.log(`Total unique TradFi symbols: ${allSymbols.size}`);

  // Write to JSON file
  const output = {
    source: "S&P 500 + Russell 3000 (partial)",
    fetchedAt: new Date().toISOString(),
    count: allSymbols.size,
    sp500Count: SP500_SYMBOLS.length,
    russellAdditionalCount: RUSSELL_ADDITIONAL.length,
    symbols: Array.from(allSymbols).sort(),
  };

  const { mkdirSync, writeFileSync } = await import("fs");
  const outputDir = join(__dirname, "data");
  mkdirSync(outputDir, { recursive: true });

  const outputPath = join(outputDir, "tradfi-symbols.json");
  writeFileSync(outputPath, JSON.stringify(output, null, 2));
  console.log(`Wrote TradFi symbols to ${outputPath}`);

  // Also write a simple list
  const listPath = join(outputDir, "tradfi-symbols-list.txt");
  writeFileSync(listPath, Array.from(allSymbols).sort().join("\n"));
  console.log(`Wrote list to ${listPath}`);
}

main();
